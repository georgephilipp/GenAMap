/**
 * The enumerated distributions.
 */

public enum ExponentialFamily
{

        /**
         * Normal distribution.
         */

        NORMAL,

        /**
         * Inverse Gaussian distribution.
         */

        INVERSE_GAUSSIAN,

        /**
         * Gamma distribution.
         */

        GAMMA,

        /**
         * Beta distribution.
         */

        BETA,

        /**
         * Poisson distribution.
         */

        POISSON,

        /**
         * Binomial distribution.
         */

        BINOMIAL,

        /**
         * Negative binomial distribution.
         */

        NEGATIVE_BINOMIAL
}
